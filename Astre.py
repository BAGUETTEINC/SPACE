

class Astre(self, Nom=None, Taille=None, Mere= None, Orbite=None):
    self.Nom = Nom
    self.Taille = Taille
    self.Orbite = Orbite
    self.Mere = Mere;

def set_Nom(self, nouveau_Nom):
    self.Nom = nouveau_Nom

def set_Orbite(self, nouvelle_Taille):
    self.Taille = nouvelle_Taille

def set_Orbite(self, nouvelle_Orbite):
    self.Orbite = nouvelle_Orbite

def set_Mere(self, nouvelle_Mere):
    self.Mere = nouvelle_Mere


def get_Nom(self):
    return self.Nom


def get_Taille(self):
    return self.Taille

def get_Orbite(self):
    return self.Orbite

def get_Mere(self):
    return self.Mere